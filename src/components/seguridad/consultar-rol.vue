<template>
  <div>
    <p>Roles de la base de datos:</p>
    <select @change="elegirOwner" name="respaldos" id="select-box1">
      <option value="default" selected="Selected" disabled>
        --Seleccione un Owner--
      </option>
      <option
        v-for="(owner, index) in owners"
        :key="index"
        :value="owner.schema_name"
      >
        {{ owner.schema_name }}
      </option>
    </select>
    <select @change="elegirRolCreado" name="respaldos" id="select-box1">
      <option value="default" selected="Selected" disabled>
        --Seleccione un rol guardado--
      </option>
      <option v-for="(rol, index) in roles" :key="index" :value="rol">
        {{ rol.granted_role }}
      </option>
    </select>
    <button @click="crearRol" :disabled="!texto">Consultar Permisos</button>
    <br />
    <select @change="elegirPrivilegios" name="respaldos" id="select-box1">
      <option value="default" selected="Selected" disabled>
        --Permisos del rol--
      </option>
      <option
        v-for="(privilege, index) in privilegios"
        :key="index"
        :value="privilege.privilege"
      >
        {{ privilege.privilege }}
      </option>
    </select>
  </div>
</template>

<script>
import managerController from "../../controllers/managerController";

export default {
  data() {
    return {
      owners: [],
      owner_elegido: null,
      roles: [],
      rol_elegido: null,
      privilegios: [],
      texto: false,
    };
  },
  async mounted() {
    this.owners = await managerController.getSchemas();
  },
  methods: {
    async elegirOwner() {
      this.owner_elegido = event.target.value;
      this.roles = await managerController.getRol(this.owner_elegido);
    },
    async elegirRolCreado() {
      this.rol_elegido = event.target.value;
      this.texto = true;
    },
    async crearRol() {
      this.privilegios = await managerController.getPrivileges(
        this.rol_elegido
      );
    },
  },
};
</script>

